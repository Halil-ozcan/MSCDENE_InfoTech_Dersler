/*
    JS Veri Türleri
    1) string: Metin. "", '', ``
    2) number: Sayı. Tam sayı ve ondalıklı sayılar.
    3) boolean: Mantıksal değer. true, false
    4) object: Nesne. {key: value}
    5) array: Dizi. [1, 2, 3, 4]
    6) null: Boş değer.
    7) undefined: Tanımsız değer.
*/

